  void _showFontSizeDrawer(StateSetter setModalState) {
    showModalBottomSheet(
      context: context,
      barrierColor: Colors.transparent,
      builder: (context) {
        return StatefulBuilder(
          builder: (context, setState) {
            return Container(
              padding: const EdgeInsets.all(16),
              decoration: BoxDecoration(
                boxShadow: [
                  BoxShadow(
                    color: Colors.black.withOpacity(0.05),
                    offset: const Offset(0, -2),
                    blurRadius: 4,
                  ),
                ],
              ),
              child: Column(
                mainAxisSize: MainAxisSize.min,
                children: [
                  const Text(
                    'Adjust Font Size',
                    style: TextStyle(
                      fontSize: 18,
                      fontWeight: FontWeight.bold,
                    ),
                  ),
                  const SizedBox(height: 16),
                  Row(
                    mainAxisAlignment: MainAxisAlignment.center,
                    children: [
                      IconButton(
                        icon: const Icon(Icons.remove),
                        onPressed: () async {
                          if (_fontSize > 16) {
                            setModalState(() {
                              _isPreviewing = true;
                            });
                            final img = await ImageUtils.previewImageFromText(
                              _contentController.text,
                              kImageThemes[_selectedThemeIndex],
                              fontSize: _fontSize - 2,
                            );
                            setModalState(() {
                              _previewImage = img;
                              _isPreviewing = false;
                            });
                            setState(() {
                              _fontSize -= 2;
                            });
                          }
                        },
                      ),
                      Text(
                        '${_fontSize.toInt()}',
                        style: const TextStyle(
                          fontSize: 20,
                          fontWeight: FontWeight.bold,
                        ),
                      ),
                      IconButton(
                        icon: const Icon(Icons.add),
                        onPressed: () async {
                          if (_fontSize < 40) {
                            setModalState(() {
                              _isPreviewing = true;
                            });
                            final img = await ImageUtils.previewImageFromText(
                              _contentController.text,
                              kImageThemes[_selectedThemeIndex],
                              fontSize: _fontSize + 2,
                            );
                            setModalState(() {
                              _previewImage = img;
                              _isPreviewing = false;
                            });
                            setState(() {
                              _fontSize += 2;
                            });
                          }
                        },
                      ),
                    ],
                  ),
                  const SizedBox(height: 16),
                  Slider(
                    value: _fontSize,
                    min: 16,
                    max: 48,
                    divisions: 16,
                    label: _fontSize.toInt().toString(),
                    onChanged: (value) async {
                      setModalState(() {
                        _isPreviewing = true;
                      });
                      final img = await ImageUtils.previewImageFromText(
                        _contentController.text,
                        kImageThemes[_selectedThemeIndex],
                        fontSize: value,
                      );
                      setModalState(() {
                        _previewImage = img;
                        _isPreviewing = false;
                      });
                      setState(() {
                        _fontSize = value;
                      });
                    },
                  ),
                ],
              ),
            );
          },
        );
      },
    );
  }

