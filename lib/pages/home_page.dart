                        icon: Icons.save_alt,
                        label: 'Save',
                        onTap: () async {
                          try {
                            // 检查权限
                            PermissionStatus status = PermissionStatus.denied;
                            if (Platform.isAndroid) {
                              if (await Permission
                                  .manageExternalStorage.isGranted) {
                                status = PermissionStatus.granted;
                              } else {
                                status = await Permission.manageExternalStorage
                                    .request();
                                if (status.isGranted) {
                                  // 授权成功
                                } else if (status.isPermanentlyDenied) {
                                  // 引导用户去设置
                                  await openAppSettings();
                                } else {
                                  // 拒绝
                                }
                              }
                            } else if (Platform.isIOS) {
                              status = await Permission.photos.request();
                            }

                            if (!status.isGranted) {
                              if (status.isPermanentlyDenied) {
                                // 显示设置对话框
                                final openSettings = await showDialog<bool>(
                                  context: context,
                                  builder: (BuildContext context) {
                                    return AlertDialog(
                                      title: const Text('Permission Denied'),
                                      content: const Text(
                                          'Please enable storage permission in settings'),
                                      actions: <Widget>[
                                        TextButton(
                                          child: const Text('Cancel'),
                                          onPressed: () {
                                            Navigator.of(context).pop(false);
                                          },
                                        ),
                                        TextButton(
                                          child: const Text('Settings'),
                                          onPressed: () {
                                            Navigator.of(context).pop(true);
                                          },
                                        ),
                                      ],
                                    );
                                  },
                                );

                                if (openSettings == true) {
                                  await openAppSettings();
                                }
                              } else {
                                Fluttertoast.showToast(
                                  msg:
                                      'Storage permission is required to save image',
                                  toastLength: Toast.LENGTH_SHORT,
                                  gravity: ToastGravity.CENTER,
                                );
                              }
                              return;
                            }

                            setModalState(() {
                              _isPreviewing = true;
                            });

                            final file = await ImageUtils.generateImageFromText(
                              _contentController.text,
                              kImageThemes[_selectedThemeIndex],
                            );

                            final bytes = await file.readAsBytes();
                            final result = await ImageGallerySaver.saveImage(
                                bytes,
                                quality: 100,
                                name:
                                    "AI_Transfer_${DateTime.now().millisecondsSinceEpoch}");

                            setModalState(() {
                              _isPreviewing = false;
                            });

                            if (result['isSuccess']) {
                              Fluttertoast.showToast(
                                msg: 'Image saved to gallery',
                                toastLength: Toast.LENGTH_SHORT,
                                gravity: ToastGravity.CENTER,
                              );
                            } else {
                              Fluttertoast.showToast(
                                msg: 'Save failed',
                                toastLength: Toast.LENGTH_SHORT,
                                gravity: ToastGravity.CENTER,
                              );
                            }
                          } catch (e) {
                            print('Save image failed: $e');
                            setModalState(() {
                              _isPreviewing = false;
                            });
                            Fluttertoast.showToast(
                              msg: 'Save failed: $e',
                              toastLength: Toast.LENGTH_SHORT,
                              gravity: ToastGravity.CENTER,
                            );
                          }
                        },
                      ),
                    ],
                  ),
                ],
              ),
            );
          },
        );
      },
    );
