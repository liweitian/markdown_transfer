  static List<TextSpan> _parseMarkdownToTextSpans(
      String markdownText, Color textColor, double fontSize) {
    final document = md.Document(
      extensionSet: md.ExtensionSet.gitHubWeb,
      encodeHtml: false,
    );

    // 预处理文本，确保换行符统一
    final processedText =
        markdownText.replaceAll('\r\n', '\n').replaceAll('\r', '\n');

    // 预处理任务列表
    final processedLines = processedText.split('\n').map((line) {
      if (line.trim().startsWith('- [ ]')) {
        return line.replaceFirst('- [ ]', '* [ ]');
      } else if (line.trim().startsWith('- [x]') ||
          line.trim().startsWith('- [X]')) {
        return line.replaceFirst(RegExp(r'- \[[xX]\]'), '* [x]');
      }
      return line;
    }).join('\n');

    final nodes = document.parseLines(processedLines.split('\n'));
    final spans = <TextSpan>[];

    void processNode(md.Node node) {
      if (node is md.Text) {
        spans.add(TextSpan(
          text: node.text,
          style: TextStyle(
            color: textColor,
            fontSize: fontSize,
          ),
        ));
      } else if (node is md.Element) {
        switch (node.tag) {
          case 'strong':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
                fontWeight: FontWeight.bold,
              ),
            ));
            break;
          case 'em':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
                fontStyle: FontStyle.italic,
              ),
            ));
            break;
          case 'h1':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize * 1.25,
                fontWeight: FontWeight.bold,
              ),
            ));
            spans.add(const TextSpan(text: '\n\n'));
            break;
          case 'h2':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize * 1.15,
                fontWeight: FontWeight.bold,
              ),
            ));
            spans.add(const TextSpan(text: '\n\n'));
            break;
          case 'h3':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize * 1.1,
                fontWeight: FontWeight.bold,
              ),
            ));
            spans.add(const TextSpan(text: '\n\n'));
            break;
          case 'p':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'ul':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'ol':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'li':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'blockquote':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'code':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'pre':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'a':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'img':
            spans.add(TextSpan(
              text: node.textContent,
              style: TextStyle(
                color: textColor,
                fontSize: fontSize,
              ),
            ));
            break;
          case 'br':
            spans.add(TextSpan(text: '\n'));
            break;
          default:
            if (node.children != null && node.children!.isNotEmpty) {
              processNode(node.children![0]);
            }
        }
      }
    }

    for (var node in nodes) {
      processNode(node);
    }

    return spans;
  }
